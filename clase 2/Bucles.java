public class Bucles {
    public static void main (String[] args){
        for (int i =1; i <=5; i++) {
            System.out.println("Numero de iteracion: " + i);
        }
        int j=1;
        while (j <= 5) {
            System.out.println("numero de iteracion: " + j);
            j++;
        }
    }
}